package com.pruvn.rms.service.exceptions;

public class UserDeptException extends Exception {

	private static final long serialVersionUID = 1L;

	public UserDeptException() {
		super();
	}

	public UserDeptException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UserDeptException(String arg0) {
		super(arg0);
	}

	public UserDeptException(Throwable arg0) {
		super(arg0);
	}
	
}
